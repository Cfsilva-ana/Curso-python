# bool() converte valores para True/False

print("\n=== Valores True/False ===")
print("bool(1):", bool(1))        # True
print("bool(0):", bool(0))        # False
print("bool('texto'):", bool('texto'))  # True
print("bool(''):", bool(''))      # False